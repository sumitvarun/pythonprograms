#Slicing List:
#To make a slice with list, you should specify the index of the first and last elements you need to work with, so this is[:] sign of slice you should you use to make a slice in list.[first element : last element]

OS = ['Mac', 'Windows','Linux', 'Android', 'iOS']
print(OS[0:5])
print(OS[0:3])
print(OS[2:5])
